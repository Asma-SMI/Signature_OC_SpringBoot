CREATE SEQUENCE SEQ_OC_FLOW START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_OC_FLOW_DETAIL START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_OC_OPERATION START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_OC_FLOW_PAYLOAD START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_OC_DECISION_AUDIT START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_OC_INBOUND_EVENT START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_OC_OUTBOUND_EVENT START WITH 1 INCREMENT BY 1 NOCACHE;

CREATE TABLE OC_FLOW (
  ID NUMBER(19) PRIMARY KEY,
  BUSINESS_KEY VARCHAR2(50) NOT NULL UNIQUE,
  FLOW_REFERENCE VARCHAR2(50),
  FLOW_TYPE VARCHAR2(30) NOT NULL,
  SOURCE VARCHAR2(50),
  SIGNATURE_STATUS VARCHAR2(20) NOT NULL,
  STATUS VARCHAR2(30) NOT NULL,
  CORRELATION_ID VARCHAR2(100),
  INBOUND_MESSAGE_ID VARCHAR2(100) UNIQUE,
  CREATED_AT TIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP,
  RECEIVED_AT TIMESTAMP,
  FINALIZED_AT TIMESTAMP,
  VERSION NUMBER(10) NOT NULL
);
CREATE INDEX IDX_OC_FLOW_BK ON OC_FLOW(BUSINESS_KEY);
CREATE INDEX IDX_OC_FLOW_STATUS ON OC_FLOW(STATUS);
CREATE INDEX IDX_OC_FLOW_CORR ON OC_FLOW(CORRELATION_ID);
CREATE INDEX IDX_OC_FLOW_REF ON OC_FLOW(FLOW_REFERENCE);

CREATE TABLE OC_FLOW_DETAIL (
  ID NUMBER(19) PRIMARY KEY,
  FLOW_ID NUMBER(19) NOT NULL UNIQUE,
  COD_TYP_MES VARCHAR2(20), COD_TYP_DOC VARCHAR2(10), ETAT VARCHAR2(20),
  NUM_MESS_TTN VARCHAR2(50), NUM_DOSS_TTN VARCHAR2(50), NUM_DEM_TTN VARCHAR2(50),
  EMETTEUR VARCHAR2(50), DESTINATAIRE VARCHAR2(50), COD_DOU_IMP VARCHAR2(20),
  RAI_SOC_IMP VARCHAR2(200), ADRESSE_IMP VARCHAR2(500), COD_TTN_DEC VARCHAR2(20),
  NOM_SIG_DEC VARCHAR2(100), DAT_DEC DATE, COD_BUR_DOU VARCHAR2(20), LIB_BUR_DOU VARCHAR2(200),
  NUM_REP_DDM VARCHAR2(50), NUM_DEC_DDM VARCHAR2(50), DAT_DEC_DDM DATE,
  COD_BQ_IMP VARCHAR2(20), COD_ORG_IMP VARCHAR2(20), NUM_RIB VARCHAR2(30),
  NUM_ENR_OC VARCHAR2(50), DAT_ENR_OC DATE,
  MONT_PRINCIPAL NUMBER(18,3), MONT_INTERET NUMBER(18,3), MONT_TOT NUMBER(18,3),
  MONT_LETTRE VARCHAR2(500), MONT_REMISE NUMBER(18,3), DELAI_PAIE VARCHAR2(20), DAT_ECH DATE,
  COD_DEC_BQ VARCHAR2(20), LIB_DEC VARCHAR2(200), LIB_CAUTION VARCHAR2(500),
  NOM_ORG_BQ VARCHAR2(200), NOM_SIG_BQ VARCHAR2(100), DAT_SIG_BQ DATE,
  MOTIF_REJET VARCHAR2(500), NUM_QUITTANCE VARCHAR2(50), DAT_QUITTANCE DATE,
  NOM_SIG_REC VARCHAR2(100), DAT_SIG_REC DATE,
  ID_SEQ VARCHAR2(50), IND_TRANSACT VARCHAR2(10), COD_CPT VARCHAR2(30),
  LIB_BQ_IMP VARCHAR2(200), LIB_ORG_IMP VARCHAR2(200), MOTIF_ANNUL VARCHAR2(500),
  CONSTRAINT FK_OC_FLOW_DETAIL_FLOW FOREIGN KEY (FLOW_ID) REFERENCES OC_FLOW(ID)
);

CREATE TABLE OC_OPERATION (
  ID NUMBER(19) PRIMARY KEY,
  FLOW_ID NUMBER(19) NOT NULL,
  BUSINESS_KEY VARCHAR2(50), FLOW_REFERENCE VARCHAR2(50), FLOW_TYPE VARCHAR2(30),
  STATUS VARCHAR2(30) NOT NULL, LAST_DECISION VARCHAR2(20), FINALIZED NUMBER(1) NOT NULL,
  FINALIZED_AT TIMESTAMP, FINALIZED_BY VARCHAR2(50), CREATED_AT TIMESTAMP NOT NULL, UPDATED_AT TIMESTAMP,
  VERSION NUMBER(10) NOT NULL,
  CONSTRAINT FK_OC_OPERATION_FLOW FOREIGN KEY (FLOW_ID) REFERENCES OC_FLOW(ID)
);
CREATE INDEX IDX_OC_OPERATION_BK ON OC_OPERATION(BUSINESS_KEY);

CREATE TABLE OC_FLOW_PAYLOAD (
  ID NUMBER(19) PRIMARY KEY,
  FLOW_ID NUMBER(19) NOT NULL,
  PAYLOAD_TYPE VARCHAR2(30) NOT NULL,
  PAYLOAD_JSON CLOB NOT NULL,
  CREATED_AT TIMESTAMP NOT NULL,
  CREATED_BY VARCHAR2(50),
  CONSTRAINT FK_OC_PAYLOAD_FLOW FOREIGN KEY (FLOW_ID) REFERENCES OC_FLOW(ID)
);

CREATE TABLE OC_DECISION_AUDIT (
  ID NUMBER(19) PRIMARY KEY,
  FLOW_ID NUMBER(19) NOT NULL,
  OPERATION_ID NUMBER(19) NOT NULL,
  IDEMPOTENCY_KEY VARCHAR2(100) UNIQUE,
  DECISION VARCHAR2(20) NOT NULL,
  FINALIZE_FLAG NUMBER(1) NOT NULL,
  USER_ID VARCHAR2(50), ROLE_CODE VARCHAR2(50), ORG_NODE_ID VARCHAR2(50),
  REASON VARCHAR2(500), COMMENT_TEXT VARCHAR2(1000), CREATED_AT TIMESTAMP NOT NULL,
  CORRELATION_ID VARCHAR2(100),
  CONSTRAINT FK_OC_AUDIT_FLOW FOREIGN KEY (FLOW_ID) REFERENCES OC_FLOW(ID),
  CONSTRAINT FK_OC_AUDIT_OPERATION FOREIGN KEY (OPERATION_ID) REFERENCES OC_OPERATION(ID)
);
CREATE INDEX IDX_OC_DEC_AUDIT_CORR ON OC_DECISION_AUDIT(CORRELATION_ID);

CREATE TABLE OC_INBOUND_EVENT (
  ID NUMBER(19) PRIMARY KEY,
  MESSAGE_ID VARCHAR2(100) NOT NULL UNIQUE,
  CORRELATION_ID VARCHAR2(100), TOPIC VARCHAR2(100), PARTITION_NO NUMBER(10), OFFSET_NO NUMBER(19),
  STATUS VARCHAR2(20) NOT NULL, ERROR_MESSAGE VARCHAR2(1000), RECEIVED_AT TIMESTAMP NOT NULL, PROCESSED_AT TIMESTAMP
);

CREATE TABLE OC_OUTBOUND_EVENT (
  ID NUMBER(19) PRIMARY KEY,
  FLOW_ID NUMBER(19) NOT NULL,
  MESSAGE_ID VARCHAR2(100) NOT NULL UNIQUE,
  TOPIC VARCHAR2(100) NOT NULL,
  STATUS VARCHAR2(20) NOT NULL,
  PAYLOAD_JSON CLOB NOT NULL,
  ERROR_MESSAGE VARCHAR2(1000), CREATED_AT TIMESTAMP NOT NULL, SENT_AT TIMESTAMP, RETRY_COUNT NUMBER(5),
  CONSTRAINT FK_OC_OUTBOUND_FLOW FOREIGN KEY (FLOW_ID) REFERENCES OC_FLOW(ID)
);
